To run SPECPR:

     The terminal should display a login  message  like  the
following:


_L_o_g_i_n:

at which point you can login by typing in your user  id  and
password.


     The system will respond by typing

$ or your own specified prompt

then you type

specpr

and SPECPR will begin running.

     You can also start up SPECPR by typing


specpr filename

where filename is the name of your (existing) restart  file.
Starting  in this manner will skip some of the initial steps
of specpr.




















